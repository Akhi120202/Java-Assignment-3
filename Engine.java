// Define Engine class 
public class Engine {

    private String type;

    //constructor
    public Engine(String type) {
        this.type = type;
    }

    // Getter method to retrieve the engine type
    public String getType() {
        return type;
    }

    // Setter method to set the engine type
    public void setType(String type) {
        this.type = type;
    }
}
